public class Ques12 {
    public static void main(String[] args) {
        byte b = 5;
        int i = 10;
        int result1 = b + i;
        System.out.println("byte + int = " + result1);

        float f = 13.4f;
        long l = 495L;
        float result2 = f + l;
        System.out.println("float + long = " + result2);
    }
}